package chapter03;

public class Operator06 {
	public static void main(String[] args) {
        // 삼항 연산자(항이 3개) 잘 안 쓰지만 알아두면 좋음
        // 수식 ? true : false
        int a = 5;
        int b = 8;
        int result = (a > b) ? 10 : 20;
        
        System.out.println("결과: " + result);
	}
}
